public class Address {
    private String id;
    private String homeNum;
    private String street;
    private String city;
    private String country;

    public Address(String id, String homeNum, String street, String city, String country) {
        this.id = id;
        this.homeNum = homeNum;
        this.street = street;
        this.city = city;
        this.country = country;
    }
}
